/**
 * 将 A-Law 数据解码为 PCM (Float32Array)
 * @param {Uint8Array} alawData 
 * @returns {Float32Array} PCM data (-1.0 to 1.0)
 */
export function decodeALaw(alawData) {
    const len = alawData.length;
    const pcmData = new Float32Array(len);
    
    for (let i = 0; i < len; i++) {
        // 查找映射表并归一化到 -1.0 ~ 1.0
        pcmData[i] = alawMap[alawData[i]] / 32768;
    }
    
    return pcmData;
}

/**
 * 将 A-Law 数据解码为 PCM (Int16Array)
 * @param {Uint8Array} alawData 
 * @returns {Int16Array} PCM data
 */
export function decodeALawInt16(alawData) {
    const len = alawData.length;
    const pcmData = new Int16Array(len);
    
    for (let i = 0; i < len; i++) {
        pcmData[i] = alawMap[alawData[i]];
    }
    
    return pcmData;
}
